# string methods
greeting = "Hey there, looking nice !!"

# to change the string into UPPERCASE
print(greeting.upper())
# to change the string into lowercase
print(greeting.lower())

# replace word in the string
print(greeting.replace("there", "Nabasmita"))
# replace a letter in string
print(greeting.replace("e", "a"))

# returns index of first occurrence of that character or word
print(greeting.find("e"))
